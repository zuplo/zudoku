import { InfoIcon } from "lucide-react";
import { SyntaxHighlight } from "zudoku/ui/SyntaxHighlight.js";
import {
  Tooltip,
  TooltipContent,
  TooltipProvider,
  TooltipTrigger,
} from "zudoku/ui/Tooltip.js";
import { useTranslation } from "../../components/context/useTranslation.js";
import { NonHighlightedCode } from "./components/NonHighlightedCode.js";
import * as SidecarBox from "./SidecarBox.js";

export const GeneratedExampleSidecarBox = ({
  code,
  language = "json",
  isOnScreen,
  shouldLazyHighlight,
}: {
  code: string;
  language?: string;
  isOnScreen: boolean;
  shouldLazyHighlight?: boolean;
}) => {
  const { t } = useTranslation();
  return (
    <SidecarBox.Root>
      <SidecarBox.Head className="text-xs flex justify-between items-center">
        <div className="flex items-center gap-1.5">
          <span className="font-medium">{t("openapi.exampleRequestBody")}</span>
          <TooltipProvider>
            <Tooltip>
              <TooltipTrigger asChild>
                <InfoIcon size={13} />
              </TooltipTrigger>
              <TooltipContent>
                {t("openapi.exampleAutoGenerated")}
              </TooltipContent>
            </Tooltip>
          </TooltipProvider>
        </div>
      </SidecarBox.Head>
      <SidecarBox.Body className="p-0">
        {shouldLazyHighlight && !isOnScreen ? (
          <NonHighlightedCode code={code} />
        ) : (
          <SyntaxHighlight
            embedded
            language={language}
            code={code}
            className="[--scrollbar-color:gray] rounded-none text-xs max-h-[200px]"
          />
        )}
      </SidecarBox.Body>
    </SidecarBox.Root>
  );
};
